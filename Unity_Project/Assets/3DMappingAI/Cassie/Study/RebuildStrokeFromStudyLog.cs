        public static void DestroyAllChildren(Transform parentTransform)
        {
            if (parentTransform == null)
            {
                Debug.LogError("Parent Transform is null.");
                return;
            }

            // Iterate through all child GameObjects
            foreach (Transform child in parentTransform)
            {
                // Destroy the child GameObject
                GameObject.Destroy(child.gameObject);
            }
        }
        public static List<LineRenderer> CreateLineRenderersFromSessionData(SessionData sessionData, Transform parentTransform)
        {
            List<LineRenderer> lineRenderers = new List<LineRenderer>();

            // Step 1: Get all IDs that should be deleted
            HashSet<int> deletedStrokeIds = new HashSet<int>();
            foreach (var systemState in sessionData.systemStates)
            {
                if (systemState.interactionType == InteractionType.StrokeDelete)
                {
                    deletedStrokeIds.Add(systemState.elementID);
                }
            }

            // Step 2: Create LineRenderers for non-deleted strokes
            foreach (var stroke in sessionData.allSketchedStrokes)
            {
                // Skip creating LineRenderer if the stroke is marked for deletion
                if (deletedStrokeIds.Contains(stroke.id))
                {
                    continue;
                }

                GameObject lineRendererObject = new GameObject("Stroke_" + stroke.id);
                LineRenderer lineRenderer = lineRendererObject.AddComponent<LineRenderer>();

                // Set the parent of the new GameObject
                lineRendererObject.transform.SetParent(parentTransform);

                // Set the LineRenderer positions
                lineRenderer.positionCount = stroke.inputSamples.Count;
                Vector3[] positions = new Vector3[stroke.inputSamples.Count];
                for (int i = 0; i < stroke.inputSamples.Count; i++)
                {
                    positions[i] = stroke.inputSamples[i];
                }
                lineRenderer.SetPositions(positions);

                // Optional: Configure additional LineRenderer properties
                lineRenderer.startWidth = 0.005f;
                lineRenderer.endWidth = 0.005f;
                lineRenderer.material = new Material(Shader.Find("Sprites/Default"));
                lineRenderer.startColor = Color.white;
                lineRenderer.endColor = Color.white;
                lineRenderers.Add(lineRenderer);
            }

            return lineRenderers;
        }

        public static List<Mesh> BakeMeshesFromLineRenderers(List<LineRenderer> lineRenderers)
        {
            List<Mesh> meshes = new List<Mesh>();

            if (lineRenderers == null || lineRenderers.Count == 0) return meshes;

            foreach (var lineRenderer in lineRenderers)
            {
                if (lineRenderer == null) continue;

                Mesh mesh = new Mesh();
                lineRenderer.BakeMesh(mesh);
                meshes.Add(mesh);
            }

            return meshes;
        }

        public void ExportMeshesToObj(List<Mesh> meshes, string pathPrefix)
        {
            if (meshes == null || meshes.Count == 0) return;

            string curves = "";
            // Dictionary to track processed materials
            Dictionary<string, string> processedMaterials = new Dictionary<string, string>();

            File.Create(pathPrefix).Dispose();
            ObjExporterScript.Start();
            int index  = 0;
            Material mat = new Material(Shader.Find("Sprites/Default"));
            foreach (Mesh mesh in meshes)
            {
                
                // Add stroke ID as a group name
                curves += string.Format("g {0}\n", index);
                index++;

                string objString = ObjExporterScript.MeshToString(
                                        mesh,
                                        mat,
                                        this.transform,
                                        objectSpace: true);
                curves += objString;
            }
            ObjExporterScript.End();
            File.WriteAllText(pathPrefix, curves);
        }
